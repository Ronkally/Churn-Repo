// math.js

/**
 * Adds two numbers together
 */
function add(a, b) {
  return a + b;
}

/**
 * Subtracts the second number from the first
 */
function subtract(a, b) {
  return a - b;
}

/**
 * Multiplies two numbers
 */
function multiply(a, b) {
  return a * b;
}

module.exports = {
  add,
  subtract,
  multiply,
};
